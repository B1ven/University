"""
Задача «Сложная формула». Описание: Запишите три числа в переменные a, b и c соответственно и создайте переменную 
f, в которую запишите результат выражения: (a * b) + (a * c). Возведите полученное число в третью степень и результат 
разделите(обычное деление) на два. Выведите его на экран(в консоль).
"""
